ll mod = 1e9 + 7;

ll binaryExponentiation(ll number, ll index)
{
    number = number % mod;
    ll answer = 1;
    while (index > 0)
    {
        // if index is odd
        if ((index & 1) == 1)
            answer = (answer * number) % mod;
        number = (number * number) % mod;
        // divide index by 2
        index >>= 1;
    }
    return answer;
}

